class cartInfos{
    constructor(contact, products){
        this.contact = contact;
        this.products = products;
    }
}

class contact{
    constructor(nom, prenom, email, adresse, ville){
        this.firstName = nom;
        this.lastName = prenom;
        this.address = adresse;
        this.city = ville;
        this.email = email;
    }
}